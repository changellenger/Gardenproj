package notice;

public class Notice {

	int notice_num;
	int admins_num;
	String notice_con;
	int noticeview;
	String noticesub;

	public int getNotice_num() {
		return notice_num;
	}

	public void setNotice_num(int notice_num) {
		this.notice_num = notice_num;
	}

	public int getAdmins_num() {
		return admins_num;
	}

	public void setAdmins_num(int admins_num) {
		this.admins_num = admins_num;
	}

	public String getNotice_con() {
		return notice_con;
	}

	public void setNotice_con(String notice_con) {
		this.notice_con = notice_con;
	}

	public int getNoticeview() {
		return noticeview;
	}

	public void setNoticeview(int noticeview) {
		this.noticeview = noticeview;
	}

	public String getNoticesub() {
		return noticesub;
	}

	public void setNoticesub(String noticesub) {
		this.noticesub = noticesub;
	}

}
